        <h1 id="title-heading">Facilities</h1>
    

<h2>Introduction</h2>

<p>
Administration of our shared computing resources and infrastructure
 is the primary task assigned to the department's technical staff.
They aim to meet all teaching needs while also pursuing the
 most effective support for the different needs of our researchers.
Feedback from all members of the department is welcome; the better the
 technical staff understands the needs of our individuals, the better
 their efforts will be for the department as a whole.
</p>

<h2>Shared Memory, Multi-processor Computing</h2>

<p>
The department maintains two shared memory, multi-processor systems
primarily suited for multi-threaded, CPU intensive programs.
The computational server <tt>euler</tt> is a Microway system that has 
four Intel Xeon E5-4627 CPUs with 40 cores and 1 TB of memory, it is running
Ubuntu Linux.
The server <tt>gauss</tt> is a Dell R510 system that has two Intel Xeon X5670 CPUs 
with 12 cores and 128 GB of memory, it is running CentOS Linux.


</p>

<p>
The department also maintains an older shared memory, multi-processor system.
The server <tt>compute</tt> is a Sun Microsystems SunFire V880, with 8 CPUs 
and 16 GB of memory running Solaris.
</p>


<h2>Compute Clusters</h2>
<p>
The department maintains a high performance cluster called <tt>cheetah</tt> with 16 nodes and 128
processor cores. Each node has two quad core Intel Xeon L5420 2.5 GHz CPUs,
16 GB RAM and a TB drive.  The nodes are connected via Gigabit Ethernet and Infiniband.
The cluster runs CentOS Linux and features a scheduled/distributed resource 
management environment using SLURM.
The department also maintains other smaller clusters.
</p>

<p>
The department also utilizes workstations from the teaching lab
 to form a compute grid.
The pool of nodes normally includes 32 identical systems
 each with an Intel i7 CPU, 4 processor cores and 8 GB of memory; by arrangement,
additional systems could be recruited to increase the total number of CPUs.
</p>

<h2>Data Storage</h2>
<p>
Storage for departmental servers and workstations is provided by a 
8GB/sec Fiber Channel Storage Area Network and Jetstor arrays. 
Online daily backups are available to the end users.
The storage is further backed up daily to our QualStar LTO tape robot.

</p>

<h2>Computing Lab</h2>

<p>
The department computing lab, located on the 3rd floor, comprises
 two, adjoining spaces.
The immediate, thirty-two seats are available to undergraduates for work
 on computer assignments.
A tiered floor, teaching lab adjoins via restricted access door.
In addition to allowing for overflow capacity and doubling as
 an after hours cluster, this second space offers the same audio/visual
 capabilities of the classrooms and may be reserved in advanced
 for teaching.
Laboratory rules and hours are located on our external webpages under 
<a href="../computinglab/">Computing Lab</a>.
</p>

<h2>Teaching Resources</h2>

<p>
The department classrooms have new Sony laser HD projectors and also
new screens with a 16:9 aspect ratio.  The classrooms 
are equipped for audio/video presentation
 using the computer, the document camera, DVD/VCR, or a user-supplied
 laptop (A/V/network cabling present).
A small, touch-driven display atop the desk incorporates system
 power control, input select, audio volume, video mute, screen and
 shade controls, and mode specific functions.
</p>

<p>
Course materials may be distributed electronically via web, e-mail,
 or shared folders.
When desired, computer assignments can be completed using department
 facilities; instructors must communicate this need to the technical staff.
High capacity printing capabilities are available for academic needs
 (e.g. mid-terms and exams).
</p>

<h2>Wired and Wireless Networking</h2>

<p>
The department is served largely by two, physical networks.
Every faculty office has an one active port for a department workstation
 and one for a personal computer, connecting to our primary network.
The wireless network [MathCS], reserved for use by department personnel,
 along with wired ports in shared offices and teaching spaces connect
 to our utility network.
In both cases, user equipment should configure network parameters
 dynamically by using DHCP.
</p>

<h2>Services</h2>

<p>
In addition to support for department workstations, scan/print/fax
 equipment, and computational facilities; the department maintains
 its own e-mail, web, file storage, and network resources.
</p>

